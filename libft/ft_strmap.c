#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*new;
	int		i;

	if (s == NULL || !f)
		return (NULL);
	i = 0;
	new = (char*)malloc((ft_strlen(s) + 1) * sizeof(char));
	if (new == 0)
		return (NULL);
	while (s[i])
	{
		new[i] = f(s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
